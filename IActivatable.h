#include <boost/shared_ptr.hpp>

namespace Neural 
{	
	class IActivatable
	{
	public:		
		virtual ~IActivatable(){};
		
		virtual void onActivate() = 0;
		
	protected:
		IActivatable(){};
		
	};
	
	typedef boost::shared_ptr< IActivatable > IActivatablePtr;
}

#endif

