#ifndef NeuralNetworkApplication_UnDeltaFunctor_h
#define NeuralNetworkApplication_UnDeltaFunctor_h


namespace Neural
{
    template <class T> struct UnDeltaFunctor
    {
        T operator()(const T& x, const T& y) const
        {
            return y + (x * y);
        }
    };
    
}

#endif
